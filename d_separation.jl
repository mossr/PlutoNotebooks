function plot_graph(g, labels; grow="", evidence=[])
	node_style = "draw, circle, fill=white"
	evidence_style = "fill=gray!50"
	evidence_index = findall(e->e ‚àà evidence, labels)
	node_styles = Dict(map(k->k=>evidence_style, evidence_index))
	edge_style = "-{latex}"
	options = !isempty(grow) ? "grow'=$grow" : ""
	
	p = plot(g, labels,
		node_style=node_style,
		node_styles=node_styles,
		edge_style=edge_style,
		options=options)
	l = 12
	p.width = !isempty(grow) ? "$(l/2)cm" : "$(l/3)cm"
	p.height = "$(l)cm"
	return p
end

